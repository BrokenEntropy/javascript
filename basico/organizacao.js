/*
 * organização do código javascript se dá em declarações de código e blocos de código.
 * 
 * declarações de código terminam em ponto e virgula. código javascript é lido da esquerda para a direita e de cima para baixo.
 * 
 * blocos de código servem para agrupar declarações de código, blocos de código são delimitados por chaves e geralmente blocos de 
 * código tem algum tipo de estrutura associada. comumente em javascript se chama blocos de código como declarações compostas.
 * 
 * podemos ter blocos de código dentro de blocos 
 */

// declaração de código.
console.log("declaração de código");

// declaração composta 
{
    console.log("bloco de código");
    {    
        console.log("bom dia");
        console.log("boa tarde");
        console.log("boa noite");
    }
}
